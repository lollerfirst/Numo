    val xs = 4.dp      // Micro spacing
    val sm = 8.dp      // Small spacing (between label and helper text)
    val md = 12.dp     // Medium spacing (between closely related elements)
    val lg = 16.dp     // Large spacing (standard padding, between list rows)
    val xl = 24.dp     // Extra large (between major sections)
    val xxl = 32.dp    // Extra extra large (above hero content)
}
